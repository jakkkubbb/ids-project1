----------- SEQUENCE AND TRIGGER FOR REGISTRATION ID WHEN NULL -----------
CREATE SEQUENCE seq_registration_id
START WITH 1
INCREMENT BY 1
NOCACHE;

CREATE OR REPLACE TRIGGER trg_registration_id
BEFORE INSERT ON registration
FOR EACH ROW
BEGIN
    IF :NEW.id IS NULL THEN
        SELECT seq_registration_id.NEXTVAL INTO :NEW.id FROM dual;
    END IF;
END;
