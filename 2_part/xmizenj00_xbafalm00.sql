----------- CREATE TABLES -----------

-- birth_number was added to PERSON as a unique identifier with specific format
CREATE TABLE person (
    id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    full_name VARCHAR2(60) NOT NULL,
    address VARCHAR2(120) NOT NULL,
    age NUMBER(3) NOT NULL,
    birth_number VARCHAR2(11) NOT NULL UNIQUE,
    CONSTRAINT chk_person_age CHECK (age BETWEEN 5 AND 100),
    CONSTRAINT chk_person_birth_number CHECK (
        REGEXP_LIKE(birth_number, '^[0-9]{6}/?[0-9]{4}$')
    )
);


CREATE TABLE team (
    id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    category VARCHAR2(30) NOT NULL,
    name VARCHAR2(80) NOT NULL UNIQUE,
    player_count NUMBER(3) DEFAULT 0 NOT NULL,
    coach_count NUMBER(3) DEFAULT 0 NOT NULL,
    CONSTRAINT chk_team_counts CHECK (player_count >= 0 AND coach_count >= 0)
);


CREATE TABLE stadium (
    id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    stadium_type VARCHAR2(30) NOT NULL,
    ownership VARCHAR2(30) NOT NULL,
    name VARCHAR2(80) NOT NULL,
    address VARCHAR2(120) NOT NULL,
    spectator_capacity NUMBER(6) NOT NULL,
    player_capacity NUMBER(4) NOT NULL,
    CONSTRAINT chk_stadium_ownership CHECK (ownership IN ('OWNED', 'RENTED')),
    CONSTRAINT chk_stadium_capacity CHECK (spectator_capacity >= 0 AND player_capacity > 0)
);



-- Generalization/Specialization:
-- PERSON is supertype, PLAYER and COACH are subtypes
-- Relations: subtype PK is also FK to PERSON.ID
CREATE TABLE player (
    id NUMBER PRIMARY KEY,
    team_id NUMBER NOT NULL,
    height_cm NUMBER(3) NOT NULL,
    weight_kg NUMBER(3) NOT NULL,
    position_name VARCHAR2(30) NOT NULL,
    jersey_number NUMBER(2) NOT NULL,
    CONSTRAINT fk_player_person FOREIGN KEY (id) REFERENCES person(id),
    CONSTRAINT fk_player_team FOREIGN KEY (team_id) REFERENCES team(id),
    CONSTRAINT chk_player_height CHECK (height_cm BETWEEN 120 AND 250),
    CONSTRAINT chk_player_weight CHECK (weight_kg BETWEEN 35 AND 200),
    CONSTRAINT chk_player_jersey CHECK (jersey_number BETWEEN 1 AND 99),
    CONSTRAINT uq_player_team_jersey UNIQUE (team_id, jersey_number)
);

CREATE TABLE coach (
    id NUMBER PRIMARY KEY,
    team_id NUMBER NOT NULL,
    role_name VARCHAR2(30) NOT NULL,
    CONSTRAINT fk_coach_person FOREIGN KEY (id) REFERENCES person(id),
    CONSTRAINT fk_coach_team FOREIGN KEY (team_id) REFERENCES team(id)
);


CREATE TABLE training (
    id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    team_id NUMBER NOT NULL,
    stadium_id NUMBER NOT NULL,
    coach_id NUMBER NOT NULL,
    training_type VARCHAR2(10) NOT NULL,
    training_date DATE NOT NULL,
    training_time VARCHAR2(5) NOT NULL,
    auto_registration CHAR(1) DEFAULT 'N' NOT NULL,
    CONSTRAINT fk_training_team FOREIGN KEY (team_id) REFERENCES team(id),
    CONSTRAINT fk_training_stadium FOREIGN KEY (stadium_id) REFERENCES stadium(id),
    CONSTRAINT fk_training_coach FOREIGN KEY (coach_id) REFERENCES coach(id),
    CONSTRAINT chk_training_type CHECK (training_type IN ('REGULAR', 'ONE_TIME')),
    CONSTRAINT chk_training_time CHECK (REGEXP_LIKE(training_time, '^(0[0-9]|1[0-9]|2[0-3]):[0-5][0-9]$')),
    CONSTRAINT chk_training_auto CHECK (auto_registration IN ('Y', 'N'))
);


CREATE TABLE match_event (
    id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    team_id NUMBER NOT NULL,
    stadium_id NUMBER,
    away_address VARCHAR2(150),
    home_away CHAR(1) NOT NULL,
    opponent_name VARCHAR2(80) NOT NULL,
    match_date DATE NOT NULL,
    match_time VARCHAR2(5) NOT NULL,
    roster_capacity NUMBER(3) NOT NULL,
    result_text VARCHAR2(20),
    CONSTRAINT fk_match_team FOREIGN KEY (team_id) REFERENCES team(id),
    CONSTRAINT fk_match_stadium FOREIGN KEY (stadium_id) REFERENCES stadium(id),
    CONSTRAINT chk_match_home_away CHECK (home_away IN ('H', 'A')),
    CONSTRAINT chk_match_time CHECK (REGEXP_LIKE(match_time, '^(0[0-9]|1[0-9]|2[0-3]):[0-5][0-9]$')),
    CONSTRAINT chk_match_roster CHECK (roster_capacity > 0),
    CONSTRAINT chk_match_place CHECK (
        (home_away = 'H' AND stadium_id IS NOT NULL AND away_address IS NULL) OR
        (home_away = 'A' AND stadium_id IS NULL AND away_address IS NOT NULL)
    )
);


CREATE TABLE registration (
    id NUMBER PRIMARY KEY,
    status_name VARCHAR2(20) NOT NULL,
    event_type VARCHAR2(10) NOT NULL,
    player_id NUMBER NOT NULL,
    match_id  NUMBER,
    training_id NUMBER,
    created_by_player_id NUMBER,
    created_by_coach_id NUMBER,
    CONSTRAINT fk_registration_player FOREIGN KEY (player_id) REFERENCES player(id),
    CONSTRAINT fk_registration_match FOREIGN KEY (match_id) REFERENCES match_event(id),
    CONSTRAINT fk_registration_training FOREIGN KEY (training_id) REFERENCES training(id),
    CONSTRAINT fk_registration_created_player FOREIGN KEY (created_by_player_id) REFERENCES player(id),
    CONSTRAINT fk_registration_created_coach FOREIGN KEY (created_by_coach_id) REFERENCES coach(id),
    CONSTRAINT chk_registration_status CHECK (status_name IN ('REGISTERED', 'CANCELLED', 'WAITING')),
    CONSTRAINT chk_registration_event_type CHECK (event_type IN ('MATCH', 'TRAINING')),
    -- exactly one event target
    CONSTRAINT chk_registration_event CHECK (
        (match_id IS NOT NULL AND training_id IS NULL) OR
        (match_id IS NULL AND training_id IS NOT NULL)
    ),
    -- event_type must match selected event
    CONSTRAINT chk_registration_event_consistency CHECK (
        (event_type = 'MATCH' AND match_id IS NOT NULL AND training_id IS NULL) OR
        (event_type = 'TRAINING' AND training_id IS NOT NULL AND match_id IS NULL)
    ),
    -- exactly one author
    CONSTRAINT chk_registration_author CHECK (
        (created_by_player_id IS NOT NULL AND created_by_coach_id IS NULL) OR
        (created_by_player_id IS NULL AND created_by_coach_id IS NOT NULL)
    ),
    -- if created by player, it must be self-registration
    CONSTRAINT chk_registration_self_player CHECK (
        created_by_player_id IS NULL OR created_by_player_id = player_id
    ),
    -- match registration must be done by coach (not by player)
    CONSTRAINT chk_registration_match_author CHECK (
        (event_type = 'MATCH' AND created_by_coach_id IS NOT NULL AND created_by_player_id IS NULL) OR
        (event_type = 'TRAINING')
    )
);
