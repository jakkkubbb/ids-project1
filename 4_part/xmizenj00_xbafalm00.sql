----------- TRIGGERS -----------

-- Trigger 1:
-- If a training is created with auto_registration = 'Y',
-- all players of that team are automatically registered.
CREATE OR REPLACE TRIGGER trg_training_auto_registration
AFTER INSERT ON training
FOR EACH ROW
WHEN (NEW.auto_registration = 'Y')
BEGIN
    INSERT INTO registration (
        status_name,
        event_type,
        player_id,
        training_id,
        created_by_coach_id
    )
    SELECT
        'REGISTERED',
        'TRAINING',
        p.id,
        :NEW.id,
        :NEW.coach_id
    FROM player p
    WHERE p.team_id = :NEW.team_id
      AND NOT EXISTS (
          SELECT 1
          FROM registration r
          WHERE r.player_id = p.id
            AND r.training_id = :NEW.id
            AND r.event_type = 'TRAINING'
      );
END;
/


-- Trigger 2:
-- Checks capacity limits for match on INSERT or UPDATE to registration.
CREATE OR REPLACE TRIGGER trg_registration_capacity
BEFORE INSERT OR UPDATE ON registration
FOR EACH ROW
DECLARE
    v_limit NUMBER;
    v_count NUMBER;
    v_player_team_id NUMBER;
    v_event_team_id NUMBER;
BEGIN
    SELECT team_id INTO v_player_team_id
    FROM player
    WHERE id = :NEW.player_id;

    IF :NEW.status_name = 'REGISTERED' AND :NEW.event_type = 'MATCH' THEN
        SELECT team_id INTO v_event_team_id
        FROM match_event
        WHERE id = :NEW.match_id;

        IF v_player_team_id <> v_event_team_id THEN
            RAISE_APPLICATION_ERROR(-20012, 'Player must belong to the same team as match.');
        END IF;

        SELECT roster_capacity INTO v_limit
        FROM match_event
        WHERE id = :NEW.match_id;

        SELECT COUNT(*) INTO v_count
        FROM registration r
        WHERE r.event_type = 'MATCH'
          AND r.match_id = :NEW.match_id
          AND r.status_name = 'REGISTERED'
          AND ( :NEW.id IS NULL OR r.id <> :NEW.id );

        IF v_count >= v_limit THEN
            RAISE_APPLICATION_ERROR(-20010, 'Match roster capacity exceeded.');
        END IF;
    END IF;
END;
/


----------- PROCEDURES -----------

-- Procedure 1:
-- Registers one player to match with checks on coach role and team.
CREATE OR REPLACE PROCEDURE pr_register_player_for_match (
    p_player_id IN player.id%TYPE,
    p_match_id IN match_event.id%TYPE,
    p_coach_id IN coach.id%TYPE
)
IS
    v_player player%ROWTYPE;
    v_match_team_id match_event.team_id%TYPE;
    v_coach coach%ROWTYPE;
BEGIN
    SELECT * INTO v_player FROM player WHERE id = p_player_id;
    SELECT team_id INTO v_match_team_id FROM match_event WHERE id = p_match_id;
    SELECT * INTO v_coach FROM coach WHERE id = p_coach_id;

    IF v_player.team_id <> v_match_team_id THEN
        RAISE_APPLICATION_ERROR(-20020, 'Player is not in the same team as the match.');
    END IF;

    IF v_coach.team_id <> v_match_team_id THEN
        RAISE_APPLICATION_ERROR(-20021, 'Coach is not in the same team as the match.');
    END IF;

    IF v_coach.role_name <> 'HEAD_COACH' THEN
        RAISE_APPLICATION_ERROR(-20022, 'Only HEAD_COACH can register players for match.');
    END IF;

    INSERT INTO registration (
        status_name,
        event_type,
        player_id,
        match_id,
        created_by_coach_id
    )
    VALUES (
        'REGISTERED',
        'MATCH',
        p_player_id,
        p_match_id,
        p_coach_id
    );

    DBMS_OUTPUT.PUT_LINE('pr_register_player_for_match: registration created');
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('pr_register_player_for_match: player/match/coach not found');
    WHEN DUP_VAL_ON_INDEX THEN
        DBMS_OUTPUT.PUT_LINE('pr_register_player_for_match: duplicate registration');
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('pr_register_player_for_match: ' || SQLERRM);
END;
/


-- Procedure 2:
-- Registers all players of training team for selected training.
CREATE OR REPLACE PROCEDURE pr_auto_register_training_players (
    p_training_id IN training.id%TYPE,
    p_coach_id IN coach.id%TYPE
)
IS
    v_team_id training.team_id%TYPE;

    CURSOR c_players (c_team_id player.team_id%TYPE) IS
        SELECT id
        FROM player
        WHERE team_id = c_team_id;
BEGIN
    SELECT team_id INTO v_team_id
    FROM training
    WHERE id = p_training_id;

    FOR r_player IN c_players(v_team_id) LOOP
        BEGIN
            INSERT INTO registration (
                status_name,
                event_type,
                player_id,
                training_id,
                created_by_coach_id
            )
            VALUES (
                'REGISTERED',
                'TRAINING',
                r_player.id,
                p_training_id,
                p_coach_id
            );
        EXCEPTION
            WHEN DUP_VAL_ON_INDEX THEN
                NULL;
            WHEN OTHERS THEN
                DBMS_OUTPUT.PUT_LINE('pr_auto_register_training_players item error: ' || SQLERRM);
        END;
    END LOOP;

    DBMS_OUTPUT.PUT_LINE('pr_auto_register_training_players: finished');
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('pr_auto_register_training_players: training not found');
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('pr_auto_register_training_players: ' || SQLERRM);
END;
/


