----------- WITH, CASE SELECT -----------

-- This query returns registration summary by team and event type,
-- and classifies level based on number of registrations.
WITH team_event_summary AS (
    SELECT t.id AS team_id,
           t.name AS team_name,
           r.event_type,
           COUNT(*) AS registration_count
    FROM registration r
    JOIN player p ON p.id = r.player_id
    JOIN team t ON t.id = p.team_id
    WHERE r.status_name = 'REGISTERED'
    GROUP BY t.id, t.name, r.event_type
)
SELECT team_id,
       team_name,
       event_type,
       registration_count,
       CASE
           WHEN registration_count >= 10 THEN 'HIGH'
           WHEN registration_count >= 5 THEN 'MEDIUM'
           ELSE 'LOW'
       END AS load_level
FROM team_event_summary
ORDER BY team_id, event_type;


